//Comparison Operators\

//Equal to ==  'is equal'
'3' == 3;
console.log('3' == 3);

//Strict Equal to  ===  'is stictly equal'
//Compares the data type and value of the two values to check if they are equal.

//true
console.log(3 === 3);

//false
console.log('3' === 3);

//Not equal to   !=
console.log('3' != 3); // false because is looking at the values.  '3' does = 3

//Strict not equal to !==
console.log('3' !== 3);// true because one is a string and one is a number


//Greater than >
console.log(3 > 2);//true

//Less than <
console.log(2 < 3);//true

//Greated than or equal to  >=
console.log(3 >= 2); //true
console.log(3 >= 4);//false

//Less than or equal to <=
console.log(2 <= 3);//true
console.log(2 <= 1);//false

//'And' operator  &&
2 && 3;

//'Or' Operator ||
2 || 3;
